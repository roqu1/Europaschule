package vektoren;

import java.io.IOException;

import oop.IO;

public class SuchenInArray {

	public static void main(String[] args) throws IOException {
		System.out.println("Bitte geben Sie ein Array bestehend aus 5 Kommazahlen ein");
		double[] werte = IO.readDoubleArray1D();
		System.out.print("Nach welcher Zahl wollen Sie suchen?");
		double suchZahl = IO.readDouble();
		for (int i=0;i<5;i++) {
		if (suchZahl != werte[i]) {
			System.out.println("Nicht treffer");
		}
		else {
			System.out.println("Treffer");
		}
		}
	}

}


